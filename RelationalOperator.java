public class RelationalOperator{
	public static void main(String[]args) {
		//演示关系运算符
		int a = 9;
		int b = 8;
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);
		boolean flag = a > b;
		System.out.println("flag ="+ flag);
		
	}
}